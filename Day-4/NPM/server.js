// | Point               | npm                    | Yarn                                                           |
// | ------------------- | ---------------------- | -------------------------------------------------------------- |
// | Full form           | Node Package Manager   | Yarn package manager                                           |
// | Comes with Node.js? | Haan, default aata hai | Nahi, alag se install karna padta hai                          |
// | Install command     | `npm install`          | `yarn install`                                                 |
// | Add package         | `npm install axios`    | `yarn add axios`                                               |
// | Dev dependency      | `npm install jest -D`  | `yarn add jest -D`                                             |
// | Remove package      | `npm uninstall axios`  | `yarn remove axios`                                            |
// | Lock file           | `package-lock.json`    | `yarn.lock`                                                    |
// | Speed               | Ab kaafi fast hai      | Usually fast, especially caching ke saath                      |
// | Security            | `npm audit` built-in   | Yarn me bhi audit/support options version pe depend karte hain |
// | Popularity          | Most common/default    | Popular, especially older/large projects                       |
